const savedLocale = localStorage.getItem("r2-explorer-locale");
const browserLocale = navigator.language?.toLowerCase().startsWith("zh")
	? "zh-CN"
	: "en-US";

export const localeState = reactive({
	locale: messages[savedLocale] ? savedLocale : browserLocale,
});

export function t(key, params = {}) {
	const template = messages[localeState.locale][key] || messages["en-US"][key] || key;
	return Object.entries(params).reduce(
		(result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
		template,
	);
}

export function setLocale(locale) {
	localeState.locale = messages[locale] ? locale : "en-US";
	localStorage.setItem("r2-explorer-locale", localeState.locale);
	document.documentElement.lang = localeState.locale;
}

export function formatDate(value, options) {
	return new Intl.DateTimeFormat(localeState.locale, options).format(new Date(value));
}

export function formatDateTime(value) {
	return new Intl.DateTimeFormat(localeState.locale, {
		dateStyle: "medium",
		timeStyle: "medium",
	}).format(new Date(value));
}

export function formatRelativeTime(value, unit) {
	return new Intl.RelativeTimeFormat(localeState.locale, {
		numeric: "always",
	}).format(value, unit);
}

setLocale(localeState.locale);
